% # 使用说明
% # 1、输入：列向量x,y,可做修改
% # 2、输出：输出结果为二元正态分布的五个参数：横坐标均值，横坐标方差，纵坐标均值，纵坐标方差，横纵坐标相关系数
% #################################################

x=[0;1;2];
y=[0;1;2];
ex=mean(x)
dx=var(x);
ey=mean(y);
dy=var(y);
z=x.*y;
ez=mean(z);
cxy=ez-ex*ey;
p=cxy/(sqrt(dx)*sqrt(dy));
